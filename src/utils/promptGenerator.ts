  /**
   * 生成SORA2可用的电商视频Prompt
   * @param userInput - 用户输入参数
   * @returns 生成后的SORA2 Prompt
   */
  static generatePrompt(userInput: UserInput): string {
    const { productName, sellingPoints, category = '通用', duration } = userInput;

    // 验证必填参数
    if (!productName || !productName.trim()) {
      throw new Error('产品名称不能为空');
    }
    if (!sellingPoints || sellingPoints.length === 0) {
      throw new Error('至少需要提供1个核心卖点');
    }
    if (duration !== 10 && duration !== 15) {
      throw new Error('视频时长只支持10秒或15秒');
    }

    // 获取对应品类和时长的模板
    const categoryTemplates = this.promptTemplates[category] || this.promptTemplates['通用'];
    let template = categoryTemplates[duration];

    // 如果没有找到模板，使用通用模板
    if (!template) {
      template = this.promptTemplates['通用'][duration];
    }

    // 替换产品名称
    let prompt = template.replace(/\{产品名称\}/g, productName);

    // 替换核心卖点
    sellingPoints.forEach((point, index) => {
      const placeholder = `{核心卖点${index + 1}}`;
      prompt = prompt.replace(new RegExp(placeholder, 'g'), point);
    });

    // 如果卖点不足，移除多余的占位符
    for (let i = sellingPoints.length + 1; i <= 3; i++) {
      const placeholder = `{核心卖点${i}}`;
      prompt = prompt.replace(new RegExp(placeholder, 'g'), '产品特色');
    }

    return prompt;
  }

  /**
   * 获取所有支持的品类
   */
  static getCategories(): ProductCategory[] {
    return ['通用', '美妆', '3C', '食品', '家居', '服饰'];
  }

  /**
   * 获取支持的时长选项
   */
  static getDurations(): VideoDuration[] {
    return [10, 15];
  }
